<template>
  <div>
    <list
      class="list-main box-shadow"
      :columns="columns"
      :loading="loading"
      :list="list"
      index
      @handle-size="handleSize"
      @handle-current="handleCurrent"
      @row-click="rowClick"
    />

  </div>
</template>

<script>import { mapGetters } from 'vuex'
import { getProjectCommissionList, deleteProjectCommission } from '@/api/poject/index'
import List from '@/components/List'

export default {
  components: {
    List
  },
  computed: {
    ...mapGetters(['node'])
  },
  data() {
    return {
      loading: false,
      list: {},
      columns: [
        { text: '提成或方案名称', name: 'fschemename' },
        { text: '日期', name: 'fdate' },
        { text: '计算金额字段', name: 'famount' },
        { text: '提成方式', name: 'fcommissionmethod' },
        { text: '固定提成方式', name: 'ffixedcommissionmethod' },
        { text: '固定提成值', name: 'ffixedcommissionvalue' },
        { text: '使用部门', name: 'fapplicabledepartment' },
        { text: '使用岗位', name: 'fapplicableposition' },
        { text: '使用项目类型', name: 'fapplicableprojecttypes' },
        { text: '状态', name: 'fstatus' },
      ]
    }
  },
  methods: {
    // 监听每页显示几条
    handleSize(val) {
      this.list.size = val
      this.$emit('uploadList')
    },
    // 监听当前页
    handleCurrent(val) {
      this.list.current = val
      this.$emit('uploadList')
    },
    dblclick(obj) {
      this.$emit('showDialog', obj.row)
    },
    Delivery(val) {
      deleteProjectCommission(val).then(res => {
        if (res.flag) {
          this.$store.dispatch('list/setClickData', '')
          this.$emit('uploadList')
        }
      })
    },
    // 监听单击某一行
    rowClick(obj) {
      this.$store.dispatch('list/setClickData', obj.row)
    },
    uploadPr(val) {
      this.fetchData(val, {
        pageNum: 1,
        pageSize: this.list.size || 50
      })
    },
    fetchData(val, data = {
      pageNum: this.list.current || 1,
      pageSize: this.list.size || 50
    }) {
      this.loading = true
      getProjectCommissionList(data, val).then(res => {
        this.loading = false
        this.list = res.data
      })
    }
  }
}
</script>

